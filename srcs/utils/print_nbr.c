#include "philosophers.h"

void	print_nbr(long nbr)
{
	char		tmp[21];
	size_t		i;

	i = 21;
	tmp[--i] = 0;
	while (nbr != 0 || i == 11)
	{
		tmp[--i] = "9876543210123456789"[(nbr % 10) + 9];
		if (nbr < 0 && nbr > -10)
			tmp[--i] = '-';
		nbr /= 10;
	}
	write(1, &tmp[i], 21 - i);
}
